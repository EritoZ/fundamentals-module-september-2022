morse_code = {".-": "A", "-.": "N",
              "-...": "B", "---": "O",
              "-.-.": "C", ".--.": "P",
              "-..": "D", "--.-": "Q",
              ".": "E", ".-.": "R",
              "..-.": "F", "...": "S",
              "--.": "G", "-": "T",
              "....": "H", "..-": "U",
              "..": "I", "...-": "V",
              ".---": "J", ".--": "W",
              "-.-": "K", "-..-": "X",
              ".-..": "L", "-.--": "Y",
              "--": "M", "--..": "Z",
                         "|": " "}

code = input().split()

for i, symbol in enumerate(code):
    code[i] = morse_code[symbol]

print("".join(code))
